/*
TODO: Now we are create a program Cat age to human age. The formula to convert cat years to human years is: Human Age = (Cat Age - 2) * 4 + 21. This formula accounts for the fact that cats mature more quickly in their first two years of life, and then age more slowly after that.
*/

// Step 1: Create a variable to store the cat's age
var catAge = 1;
// Step 2: Convert cat age to human age using the formula
var humanAge = (catAge - 2) * 4 + 21;
// Step 3: Output the human age
console.log("The cat's age in human years is: " + humanAge);